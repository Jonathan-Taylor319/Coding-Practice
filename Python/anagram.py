# Challenge: Group Anagrams
# Problem Statement:
# Given an array of strings, group the anagrams together. An anagram is a word formed by rearranging the letters of another word
#  (e.g., "eat" and "tea" are anagrams). You can return the groups in any order.

# Example 1:
# Input: ["eat", "tea", "tan", "ate", "nat", "bat"]
# Output:

# Copy
# [
#   ["eat", "tea", "ate"],
#   ["tan", "nat"],
#   ["bat"]
# ]
# Example 2:
# Input: [""]
# Output: [[""]]

# Example 3:
# Input: ["a"]
